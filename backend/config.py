"""
config.py — центральный файл настроек проекта.

Здесь собраны ВСЕ параметры: пути, ключи, адреса серверов.
Любой другой модуль импортирует нужные значения отсюда —
так настройки не разбросаны по коду, а лежат в одном месте.
"""

import os
from pathlib import Path

# ─── Пути ──────────────────────────────────────────────────────────────────
#
# Пути прописаны абсолютно (жёстко на диск D:), а не вычисляются через
# __file__. Причина: хуки Claude Code запускаются из ЛЮБОЙ директории,
# и относительные вычисления путей в таком случае ломаются.

BASE_DIR = Path("D:/mind/palaces-of-the-mind")          # корень проекта
DATA_DIR = Path("D:/mind/palaces-of-the-mind/data")     # папка с базой
LOGS_DIR = Path("D:/mind/palaces-of-the-mind/logs")     # папка с логами
DB_PATH  = Path("D:/mind/palaces-of-the-mind/data/memory.db")  # файл SQLite

# Создаём папки, если их ещё нет (exist_ok=True — не падать, если уже есть).
DATA_DIR.mkdir(parents=True, exist_ok=True)
LOGS_DIR.mkdir(parents=True, exist_ok=True)

# ─── Секреты из файла .env ──────────────────────────────────────────────────
#
# Файл .env лежит рядом и в git НЕ попадает (см. .gitignore) — поэтому
# ключ безопаснее держать там, а не прямо в этом файле.
# Скопируй .env.example в .env и впиши ключ.
try:
    from dotenv import load_dotenv
    load_dotenv(BASE_DIR / ".env")
except ImportError:
    # python-dotenv ещё не установлен — не страшно, ключ возьмём из окружения.
    pass

# ─── LLM API (Google Gemini) ────────────────────────────────────────────────
#
# Проект ходит в Google Gemini через его OpenAI-совместимый эндпоинт —
# поэтому весь код работает на той же библиотеке openai, без изменений.
#
# Имена LLM_* специально нейтральны: чтобы сменить провайдера
# (OpenRouter, Groq, NVIDIA) — достаточно поменять три строки ниже,
# трогать остальной код не нужно.
#
# Ключ ищется в .env / переменных окружения (LLM_API_KEY); если пусто —
# берётся заглушка. Ключ Gemini бесплатно: https://aistudio.google.com/apikey

LLM_API_KEY  = os.getenv("LLM_API_KEY", "ВСТАВЬ_СЮДА_СВОЙ_КЛЮЧ")
LLM_BASE_URL = "https://generativelanguage.googleapis.com/v1beta/openai/"
LLM_MODEL    = "gemini-2.5-flash"   # для этого ключа бесплатный тариф есть именно у 2.5-flash

# Таймаут запроса к LLM в секундах — чтобы хук не висел вечно.
LLM_TIMEOUT = 30

# ─── FastAPI сервер ─────────────────────────────────────────────────────────

API_HOST = "127.0.0.1"   # только локальный доступ, наружу сервер не торчит
API_PORT = 8765

# Секретный токен для защиты API. Его генерирует Electron при запуске и
# передаёт серверу (через переменную окружения PALACES_TOKEN) и окну
# приложения. Тогда каждый запрос к /api/* обязан прислать заголовок
# X-Palaces-Token с этим значением — посторонняя страница его не знает.
# Если токен пустой (сервер запущен вручную) — проверка отключается.
API_TOKEN = os.getenv("PALACES_TOKEN", "")

# ─── Поиск по памяти ────────────────────────────────────────────────────────

MAX_SEARCH_RESULTS = 5   # сколько максимум записей возвращать при поиске

# ─── Фоновый разбор сессий ──────────────────────────────────────────────────
#
# Хук Stop срабатывает на каждый ответ ассистента, но сам LLM-разбор
# делается НЕ в хуке, а фоновым «уборщиком» внутри сервера. Уборщик берёт
# сессии, которые уже «затихли» (диалог, видимо, закончен), и разбирает
# каждую один раз. Так один диалог = один запрос к LLM, а не на каждый ход.

# Сколько минут «тишины» — чтобы считать сессию завершённой и готовой к разбору.
SESSION_IDLE_MINUTES = 5

# Как часто уборщик проверяет, нет ли затихших сессий (в секундах).
SWEEP_INTERVAL_SECONDS = 120

# Сколько сессий разбирать за одну проверку — бережём лимит запросов LLM.
SWEEP_MAX_PER_RUN = 3


def is_llm_key_set() -> bool:
    """
    Проверяет, задан ли реальный ключ LLM.

    Возвращает False, если ключ ещё не вписан (стоит заглушка) —
    тогда модули, использующие LLM, просто пропустят обращение к API.
    """
    return bool(LLM_API_KEY) and LLM_API_KEY != "ВСТАВЬ_СЮДА_СВОЙ_КЛЮЧ"
