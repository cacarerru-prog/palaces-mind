def _existing_summaries(conn, subtopic_id: int) -> set:
    """Возвращает множество summary, уже сохранённых в подтеме — для идемпотентности."""
    rows = conn.execute(
        "SELECT summary FROM knowledge_nodes WHERE subtopic_id = ?",
        (subtopic_id,),
    ).fetchall()
    return {r["summary"] for r in rows}


def main() -> None:
    """Заполняет базу узлами PM-знаний. Безопасно для повторного запуска."""
    init_db(DB_PATH)
    conn = connect(DB_PATH)
    added = 0
    skipped = 0
    try:
        for node in NODES:
            topic_id = get_or_create_topic(conn, node["topic"])
            subtopic_id = get_or_create_subtopic(conn, topic_id, node["subtopic"])
            existing = _existing_summaries(conn, subtopic_id)
            if node["summary"] in existing:
                skipped += 1
                continue
            save_knowledge_node(
                conn,
                subtopic_id=subtopic_id,
                summary=node["summary"],
                detail=node.get("detail", ""),
                keywords=node.get("keywords", []),
                session_id=None,
            )
            added += 1
    finally:
        conn.close()

    total = len(NODES)
    print(f"Готово: всего в наборе {total}, добавлено {added}, пропущено как дубли {skipped}")


if __name__ == "__main__":
    main()
